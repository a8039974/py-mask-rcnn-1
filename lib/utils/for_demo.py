def vis_detections(im, class_name, dets, thresh=0.3):

    im = im[:, :, (2, 1, 0)]
    for i in xrange(np.minimum(10, dets.shape[0])):
        bbox = dets[i, :4]
        score = dets[i, -1]
        if score > thresh:
            plt.cla()
            plt.imshow(im)
            plt.gca().add_patch(
                plt.Rectangle((bbox[0], bbox[1]),
                              bbox[2] - bbox[0],
                              bbox[3] - bbox[1], fill=False,
                              edgecolor='g', linewidth=3)
                )
            plt.title('{}  {:.3f}'.format(class_name, score))
            plt.show()

def im_proccess(im_orig):
    im = im_orig.astype(np.float32,copy=True)
    im -= cfg.PIXEL_MEANS
    im_shape = im.shape
    im_size_min = np.min(im_shape[0:2])
    im_size_max = np.max(im_shape[0:2])
    for target_size in cfg.TEST.SCALES:
        im_scale = float(target_size) / float(im_size_min)
        if np.round(im_scale * im_size_max) > cfg.TEST.MAX_SIZE:
            im_scale = float(cfg.TEST.MAX_SIZE) / float(im_size_max)
        im = cv2.resize(im, None, None, fx=im_scale, fy=im_scale,interpolation=cv2.INTER_LINEAR)
    im_blob = np.expand_dims(im,axis=0)
    channel_swap = (0, 3, 1, 2)
    im_blob = im_blob.transpose(channel_swap)
    im_info = np.array([[im_blob.shape[2], im_blob.shape[3], im_scale]],dtype=np.float32)
    return im_blob, im_info, im_scale

                
